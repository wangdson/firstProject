package com.dxs.Entity;

public class Mart {
	private String martId;
	private String paperId;
	private String martFlag;
	private int num;
	
	
	public String getMartFlag() {
		return martFlag;
	}
	public void setMartFlag(String martFlag) {
		this.martFlag = martFlag;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public void setMartId(String martId) {
		this.martId = martId;
	}
	public String getMartId() {
		return martId;
	}
	public void setPaperId(String paperId) {
		this.paperId = paperId;
	}
	public String getPaperId() {
		return paperId;
	}
	
	
	

}
